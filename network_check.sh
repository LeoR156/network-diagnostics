#!/bin/bash

DATE=$(date +"%Y-%m-%d_%H-%M")

# Формируем имя файла отчета
REPORT_FILE="network_report_${DATE}.txt"

echo "Начинаем диагностику сети... Пожалуйста, подождите."

{
    echo "========================================="
    echo "    ОТЧЕТ О ДИАГНОСТИКЕ СЕТИ"
    echo "    Время создания: $(date)"
    echo "========================================="
    echo ""

    echo "[1] Проверка сетевого подключения (ping 8.8.8.8)"
    ping -c 4 8.8.8.8
    echo ""
    echo "-----------------------------------------"

    echo "[2] Проверка DNS-резолвинга (dig google.com)"
    dig +short google.com
    echo ""
    echo "-----------------------------------------"

    echo "[3] Внешний IP-адрес"
    curl -s ifconfig.me
    echo ""
    echo "-----------------------------------------"

    echo "[4] Таблица маршрутизации (ip route)"
    ip route
    echo ""
    echo "-----------------------------------------"

    echo "[5] Список открытых портов (ss -tulnp)"
    sudo ss -tulnp
    echo ""
    echo "-----------------------------------------"

    echo "[6] Доступность сервисов (HTTP 80, HTTPS 443)"
    echo "Проверка порта 80 (HTTP) на google.com:"
    nc -zvw 3 google.com 80 2>&1
    
    echo "Проверка порта 443 (HTTPS) на google.com:"
    nc -zvw 3 google.com 443 2>&1
    echo ""
    
    echo "========================================="
    echo "          ДИАГНОСТИКА ЗАВЕРШЕНА"
    echo "========================================="

} > "$REPORT_FILE"

# Сообщение о завершении скрипта
echo "Готово! Результаты сохранены в файл: $REPORT_FILE"
echo "Чтобы прочитать отчет, введи: cat $REPORT_FILE"
